/* ---- SIGNAL-CHAIN AUTO-SUGGESTIONS ---------------------------------------- */
// Pairing intelligence: given one axis, recommend plausible options for the other
// so a chain reads like a believable real-world artifact (e.g. a VHS tape shown
// on a consumer CRT, a TikTok repost on an OLED phone). Built from the period /
// medium affinity between each display family and each capture category.

function buildReverseLookup(catObj) {
  const m = {};
  for (const [cat, names] of Object.entries(catObj)) for (const n of names) m[n] = cat;
  return m;
}

export const CAPTURE_CATEGORY_OF = buildReverseLookup(CAPTURE_PRESET_CATEGORIES);
export const DISPLAY_FAMILY_OF = buildReverseLookup(DISPLAY_PRESET_CATEGORIES);

// Display family -> capture categories that historically/aesthetically belong on it.
const DISPLAY_FAMILY_TO_CAPTURE_CATS = {
  "Reference": ["Digital Codec", "Film", "Camera / Sensor"],
  "Projection": ["Film"],
  "Early TV": ["Film", "Broadcast / Transmission"],
  "CRT": ["Tape", "Broadcast / Transmission", "Camera / Sensor", "Film"],
  "Flat Panel": ["Digital Codec", "Social", "Camera / Sensor", "Glitch / Decay"],
  "Large / Public": ["Social", "Digital Codec", "Broadcast / Transmission", "Film"],
  "Portable / Retro": ["Tape", "Surveillance", "Camera / Sensor", "Broadcast / Transmission"],
};

// Capture category -> display families it was typically watched on.
const CAPTURE_CAT_TO_DISPLAY_FAMILIES = {
  "Reference": ["Flat Panel", "Reference"],
  "Film": ["Projection", "Early TV", "Large / Public", "CRT"],
  "Tape": ["CRT", "Portable / Retro"],
  "Broadcast / Transmission": ["CRT", "Early TV", "Portable / Retro", "Large / Public"],
  "Digital Codec": ["Flat Panel", "Large / Public"],
  "Camera / Sensor": ["Flat Panel", "CRT", "Portable / Retro"],
  "Surveillance": ["CRT", "Portable / Retro", "Flat Panel"],
  "Social": ["Flat Panel", "Large / Public"],
  "Glitch / Decay": ["Flat Panel", "Large / Public"],
};

/** Recommend capture/format presets that pair well with the given display. */
export function suggestCaptureForDisplay(displayName, limit = 6) {
  const fam = DISPLAY_FAMILY_OF[displayName];
  if (!fam) return [];
  const cats = DISPLAY_FAMILY_TO_CAPTURE_CATS[fam] || [];
  const out = [];
  for (const c of cats) {
    for (const n of CAPTURE_PRESET_CATEGORIES[c] || []) {
      if (n !== "Direct Digital" && !out.includes(n)) out.push(n);
    }
  }
  return out.slice(0, limit);
}

/** Recommend display devices that pair well with the given capture/format. */
export function suggestDisplayForCapture(captureName, limit = 6) {
  const cat = CAPTURE_CATEGORY_OF[captureName];
  if (!cat) return [];
  const fams = CAPTURE_CAT_TO_DISPLAY_FAMILIES[cat] || [];
  const out = [];
  for (const f of fams) {
    for (const n of DISPLAY_PRESET_CATEGORIES[f] || []) {
      if (n !== "Direct / No Display" && !out.includes(n)) out.push(n);
    }
  }
  return out.slice(0, limit);
}
